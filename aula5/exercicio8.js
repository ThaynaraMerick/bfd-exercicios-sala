function calculaAreaRetangulo (largura, altura){
    return largura * altura;
}

let area1 = calculaAreaRetangulo (5, 10);
console.log("Área do retangulo 1: ", area1);

let area2 = calculaAreaRetangulo (7, 3);
console.log("Área do retangulo 2: ", area2);

let area3 = calculaAreaRetangulo(12, 8);
console.log("Área do retangulo 3: ", area3);